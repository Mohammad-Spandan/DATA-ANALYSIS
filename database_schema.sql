-- ============================================
-- CREATE USEFUL VIEWS FOR ANALYSIS
-- ============================================

-- View 1: Complete crop data with all details
CREATE VIEW v_crop_data_with_details AS
SELECT 
    cd.data_id,
    cd.crop_id,
    c.crop_name,
    cat.category_name,
    cd.year,
    cd.area_000ac,
    cd.production,
    cd.production_unit
FROM crop_data cd
JOIN crops c ON cd.crop_id = c.crop_id
JOIN categories cat ON c.category_id = cat.category_id
ORDER BY cd.crop_id, cd.year;

-- View 2: Production trends by crop
CREATE VIEW v_production_trends AS
SELECT 
    crop_id,
    crop_name,
    MIN(year) as first_year,
    MAX(year) as last_year,
    COUNT(*) as data_points,
    AVG(production) as avg_production,
    MIN(production) as min_production,
    MAX(production) as max_production
FROM v_crop_data_with_details
GROUP BY crop_id, crop_name;

-- View 3: Area trends by crop
CREATE VIEW v_area_trends AS
SELECT 
    crop_id,
    crop_name,
    AVG(area_000ac) as avg_area,
    MIN(area_000ac) as min_area,
    MAX(area_000ac) as max_area
FROM v_crop_data_with_details
GROUP BY crop_id, crop_name;

